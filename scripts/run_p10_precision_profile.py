def _mean_metric(rec: pd.DataFrame, method: str, key: str) -> float:
    sub = rec[(rec["scene_type"] == "dynamic") & (rec["method"].str.lower() == method)]
    if sub.empty:
        return float("nan")
    vals = pd.to_numeric(sub[key], errors="coerce").to_numpy(dtype=float)
    vals = vals[np.isfinite(vals)]
    if vals.size == 0:
        return float("nan")
    return float(np.mean(vals))


def _load_tables(root: Path) -> Tuple[pd.DataFrame, pd.DataFrame]:
    candidates = [
        root / "tables",
        root / "oracle" / "tables",
        root / "slam" / "tables",
    ]
    table_dir = None
    for c in candidates:
        if (c / "reconstruction_metrics.csv").exists() and (c / "dynamic_metrics.csv").exists():
            table_dir = c
            break
    if table_dir is None:
        raise FileNotFoundError(f"missing benchmark tables under: {root}")
    rec_p = table_dir / "reconstruction_metrics.csv"
    dyn_p = table_dir / "dynamic_metrics.csv"
    rec = pd.read_csv(rec_p)
    dyn = pd.read_csv(dyn_p)
    rec["method"] = rec["method"].astype(str).str.lower()
    dyn["method"] = dyn["method"].astype(str).str.lower()
    return rec, dyn


def _eval_profile(
    profile: Profile,
    tum_rec: pd.DataFrame,
    tum_dyn: pd.DataFrame,
    bonn_rec: pd.DataFrame,
    bonn_dyn: pd.DataFrame,
    tum_tsdf_ghost: float,
    bonn_tsdf_ghost: float,
) -> Dict[str, float | str | bool]:
    tum_acc_cm = _mean_metric(tum_rec, "egf", "accuracy") * 100.0
    tum_comp_r = _mean_metric(tum_rec, "egf", "recall_5cm") * 100.0
    tum_fscore = _mean_metric(tum_rec, "egf", "fscore")
    tum_ghost = _mean_metric(tum_dyn, "egf", "ghost_ratio")
    tum_red = (tum_tsdf_ghost - tum_ghost) / max(1e-9, tum_tsdf_ghost)

    bonn_acc_cm = _mean_metric(bonn_rec, "egf", "accuracy") * 100.0
    bonn_comp_r = _mean_metric(bonn_rec, "egf", "recall_5cm") * 100.0
    bonn_fscore = _mean_metric(bonn_rec, "egf", "fscore")
    bonn_ghost = _mean_metric(bonn_dyn, "egf", "ghost_ratio")
    bonn_red = (bonn_tsdf_ghost - bonn_ghost) / max(1e-9, bonn_tsdf_ghost)

    pass_tum = bool(tum_acc_cm <= 1.80 and tum_comp_r >= 95.0)
    pass_bonn = bool(bonn_acc_cm <= 2.60 and bonn_comp_r >= 95.0)
    pass_ghost = bool(min(tum_red, bonn_red) >= 0.35)
    passed = bool(pass_tum and pass_bonn and pass_ghost)

    return {
        "profile": profile.name,
        "tum_acc_cm": tum_acc_cm,
        "tum_comp_r_5cm": tum_comp_r,
        "tum_fscore": tum_fscore,
        "tum_ghost_ratio": tum_ghost,
        "tum_ghost_reduction_vs_tsdf": tum_red,
        "bonn_acc_cm": bonn_acc_cm,
        "bonn_comp_r_5cm": bonn_comp_r,
        "bonn_fscore": bonn_fscore,
        "bonn_ghost_ratio": bonn_ghost,
        "bonn_ghost_reduction_vs_tsdf": bonn_red,
        "pass_tum": pass_tum,
        "pass_bonn": pass_bonn,
        "pass_ghost": pass_ghost,
        "pass_all": passed,
    }


