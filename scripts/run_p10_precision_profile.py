def _write_plot(path: Path, rows: List[Dict[str, object]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fig, axes = plt.subplots(1, 2, figsize=(11.8, 4.8), constrained_layout=True)
    for ax, domain, xk, yk, xthr in [
        (axes[0], "TUM walking (Oracle)", "tum_acc_cm", "tum_comp_r_5cm", 1.80),
        (axes[1], "Bonn all3 (SLAM)", "bonn_acc_cm", "bonn_comp_r_5cm", 2.60),
    ]:
        xs = np.asarray([float(r[xk]) for r in rows], dtype=float)
        ys = np.asarray([float(r[yk]) for r in rows], dtype=float)
        pass_mask = np.asarray([bool(r["pass_all"]) for r in rows], dtype=bool)
        ax.scatter(xs[~pass_mask], ys[~pass_mask], c="#d62728", s=55, label="not pass")
        ax.scatter(xs[pass_mask], ys[pass_mask], c="#2ca02c", s=75, label="pass")
        for r in rows:
            ax.annotate(str(r["profile"]), (float(r[xk]), float(r[yk])), fontsize=8, xytext=(4, 4), textcoords="offset points")
        ax.axvline(xthr, color="#1f77b4", linestyle="--", linewidth=1.0, label=f"Acc <= {xthr:.2f}cm")
        ax.axhline(95.0, color="#9467bd", linestyle="--", linewidth=1.0, label="Comp-R >= 95%")
        ax.set_title(domain)
        ax.set_xlabel("Acc(cm) lower is better")
        ax.set_ylabel("Comp-R(5cm, %) higher is better")
        ax.grid(alpha=0.25)
        ax.set_xlim(left=0.0)
        ax.set_ylim(0.0, 101.0)
    handles, labels = axes[1].get_legend_handles_labels()
    fig.legend(handles, labels, loc="lower center", ncol=4, frameon=False)
    fig.savefig(path, dpi=220)
    plt.close(fig)


def _score_for_rank(row: Dict[str, object]) -> float:
    # Lower is better. Prefer pass_all first, then lower acc and higher comp-r.
    tum_acc = float(row["tum_acc_cm"])
    bonn_acc = float(row["bonn_acc_cm"])
    tum_comp = float(row["tum_comp_r_5cm"])
    bonn_comp = float(row["bonn_comp_r_5cm"])
    ghost = min(float(row["tum_ghost_reduction_vs_tsdf"]), float(row["bonn_ghost_reduction_vs_tsdf"]))
    penalty = 0.0 if bool(row["pass_all"]) else 1000.0
    return penalty + tum_acc + bonn_acc - 0.02 * (tum_comp + bonn_comp) - 0.5 * ghost


